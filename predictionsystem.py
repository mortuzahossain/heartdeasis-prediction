    def ButtonClicked(self):
        global model
        
        age = self.age.text()
        sex = self.sex.currentIndex()
        cp = self.cp.currentIndex() + 1
        trestbps = self.trestbps.text()
        chol = self.chol.text()
        fbs = self.fbs.text()
        if fbs > 120:
            fbs = 1
        else :
            fbs = 0
        restecg = self.restecg.currentIndex()
        exang = self.exang.currentIndex()
        oldpeak = self.oldpeak.text()
        slope = self.slope.currentIndex() + 1
        ca = self.ca.currentIndex()
        thal = self.comboBox.currentIndex()
        if thal == 0 :
            thal = 3
        elif thal == 2:
            thal = 6
        else:
            thal = 7
        thalach = self.thalach.text()

        prediction = model.predict([[age,sex,cp,trestbps,chol,fbs,restecg,thalach,exang,oldpeak,slope,ca,thal]])[0]

        if prediction >= 1:
            message = "> 50% diameter narrowing"
        else:
            message = "< 50% diameter narrowing"

        self.message.setText(_translate("MainWindow", "Angiographic disease status: " + message , None))
        self.predectedresult.setText(_translate("MainWindow", "Predicted Results : %.2f" % prediction, None))

